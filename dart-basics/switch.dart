void main() {
  String language = "Dart";

  switch (language) {
    case "Dart":
      print("Current language");
      break;
    case "C":
      print("Cool language");
      break;
    case "Swift":
      print("Another cool language");
      break;
    default:
      print("unknown");
  }
}

// switch is basically an if/else statement but simpler.